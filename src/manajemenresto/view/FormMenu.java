    public void fillCombobox() {
        comboBoxMenu.removeAllItems();
        comboBoxMenu.addItem("pilih menu");
        ArrayList<Menu> menuList = menuController.getAllMenu();
        
        if (menuList != null) {
            for (Menu menu: menuList) {
                comboBoxMenu.addItem(menu.getNama());
            }
        }
    }
    
    public void resetState() {
        textNamaMenu.setText("");
        textHarga.setText("");
        SELECTED_MENU = new Menu();
        comboBoxMenu.setSelectedItem("pilih menu");
        fillCombobox();
        fillTabelMenu();
    }
    
    public void fillTabelMenu() {
        DefaultTableModel df = (DefaultTableModel)tabelMenu.getModel();
        df.getDataVector().removeAllElements();
        ArrayList<Menu> menuList = menuController.getAllMenu();
        
        if (menuList != null) {
            int no = 1;
            for (Menu menu: menuList) {
                Object[] obj = new Object[3];
                obj[0] = no;
                obj[1] = menu.getNama();
                obj[2] = menu.getHarga();
                ++no;
                df.addRow(obj);
            }
        }
    }
