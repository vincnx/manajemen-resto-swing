    private void buttonTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonTambahActionPerformed
        String namaMenu;
        double harga;
        
        namaMenu = textNamaMenu.getText();
        try {
            harga = Double.parseDouble(textHarga.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Harga harus berupa angka");
            return;
        }
        boolean status = menuController.CreateMenu(namaMenu, harga);
        
        if (!status) {
            JOptionPane.showMessageDialog(this, "Menu gagal tersimpan");
            return;
        }
        JOptionPane.showMessageDialog(this, "Menu berhasil ditambahkan");
        resetState();
        
    }//GEN-LAST:event_buttonTambahActionPerformed

    private void buttonCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCariActionPerformed
        String pilihanMenu = comboBoxMenu.getSelectedItem().toString();
        
        if (pilihanMenu.equals("pilih menu")) {
            JOptionPane.showMessageDialog(this, "Silahkan memilih menu terlebih dahulu");
            return;
        }
        
        SELECTED_MENU = menuController.getMenuByNama(pilihanMenu);
        
        if (SELECTED_MENU == null) {
            JOptionPane.showMessageDialog(this, "Gagal mendapatkan menu");
        }
        
        textNamaMenu.setText(SELECTED_MENU.getNama());
        textHarga.setText(String.valueOf(SELECTED_MENU.getHarga()));
        
    }//GEN-LAST:event_buttonCariActionPerformed

    private void buttonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonEditActionPerformed
        Menu menuBaru = new Menu();
        
        if (SELECTED_MENU.getNama() == null) {
            JOptionPane.showMessageDialog(this, "Silahkan Pilih Menu terlebih dahulu");
            return;
        }
        
        menuBaru.setNama(textNamaMenu.getText());
        try {
            menuBaru.setHarga(Double.parseDouble(textHarga.getText()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Harga harus berupa angka");
            return;
        }
        
        boolean status = menuController.updateMenuByNama(SELECTED_MENU.getNama(), menuBaru);
        
        if (!status) {
            JOptionPane.showMessageDialog(this, "Menu gagal diubah");
            return;
        }
        JOptionPane.showMessageDialog(this, "Menu berhasil diubah");
        resetState();
    }//GEN-LAST:event_buttonEditActionPerformed

